#Bitwise operator
"""
&  AND
|  OR
^  XOR
~  NOT
<< LIFT SHIFT
>> RIGHT SHIFT
"""
a=25
b=45
print('a binary value 011001')
print('b binary value 101101 ')
print('a & b',a & b)
print('a | b',a | b)
print('a ^ b',a ^ b)
print('~a a=25 : a=25+1 : a=26 : a=-26 ans is   ',~a) #a=25 : a=25+1 : a=26 : a=-26

a=10

print('a << b',a << 2)
print('10	1010	101000	40 ')

print('a >>b',a >>2)
print('10	1010	10.10	2')