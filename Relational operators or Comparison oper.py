#Relational operators or Comparison operator
"""
==  Equal 
!=  Not equal
>   Greater than
<   Lesser than
>=  Greater then or equal to
<=  Lesser than or equal to
"""

a = 10
b = 20
print('a =10 b= 20')
print ('a == b Equal',a == b)
print ('a != b Not equal',a != b)
print ('a > b Greater than',a > b)
print ('a < b Lesser than',a < b)
print ('a >= b Greater then or equal to',a >= b)
print ('a <= b Lesser than or equal to',a <= b)