#Assignment operator
"""
+=  Addition
-=  Substraction
*=  Multiplication
/=  Division
%=  Module
**= Exponentiation
//= Floor division
"""
a=125
print(a)
a+=5                 #a=a+5
print('#a=a+5',a)
a-=5                 #a=a-5
print("#a=a-5",a)
a*=5                 #a=a*5
print("#a=a*5",a)
a/=5                 #a=a/5
print('#a=a/5',a)
a//=5                #a=a//5
print('#a=a//5 quasient ',a)
a%=5                 #a=a%5
print('#a=a%5 remender ',a)
a=5
a**=5                #a=a**5
print('#a=a**5',a)