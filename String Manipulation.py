a= "saravana"
print(a)
print(len(a))
print(a[:])
print(a[1:2])
print(a[0:2])
print(a[:8])
print(a[1:])
print('only last letter ',a[-1])
print('print only last 2nd letter ', a[-2])
print(a[:-1])
print('reverse the word using "::" ',a[::-1])
print('reverse the word using "::" ',a[::])